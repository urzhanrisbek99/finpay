-- Удаление неиспользуемой колонки cards.spent.
--
-- Колонка осталась от ранней версии: при заведении карты в неё писался 0, и
-- больше её никто не читал и не обновлял. Реальный расход за месяц считается
-- по транзакциям — на сервере current_month_spent (0006), на клиенте
-- computeMonthlySpent (entities/transaction/model/stats.ts). Хранимый счётчик
-- был бы вторым источником истины, который неизбежно разъедется с первым.
--
-- Выполнить в SQL Editor целиком.

alter table public.cards drop column if exists spent;
